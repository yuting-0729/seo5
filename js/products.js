// Helper functions for e-commerce logic
const ProductDB = {
  getAll: () => PRODUCTS,
  getById: (id) => PRODUCTS.find(p => p.id === id),
  getByCategory: (cat) => cat === "all" ? PRODUCTS : PRODUCTS.filter(p => p.category === cat),
  getHotProducts: () => PRODUCTS.filter(p => p.category === "hot"),
  getRelatedProducts: (id, limit = 4) => {
    const current = ProductDB.getById(id);
    if (!current) return [];
    // We get shoes of the same category, or hot shoes if none
    return PRODUCTS
      .filter(p => p.id !== id && (p.category === current.category || p.category === "hot"))
      .slice(0, limit);
  }
};

